import * as React from "react";
import { cn } from "@/lib/utils";
import { motion, AnimatePresence } from "framer-motion";

interface CardProps extends React.HTMLAttributes<HTMLDivElement> {
  animate?: boolean;
}

const Card = React.forwardRef<HTMLDivElement, CardProps>(
  ({ className, animate = false, ...props }, ref) => {
    const [hovered, setHovered] = React.useState(false);

    return (
      <div
        ref={ref}
        className={cn(
          "relative rounded-lg border bg-card text-card-foreground shadow-sm",
          className
        )}
        onMouseEnter={() => animate && setHovered(true)}
        onMouseLeave={() => animate && setHovered(false)}
        {...props}
      >
        <AnimatePresence>
          {animate && hovered && (
            <motion.span
              className="absolute inset-0 h-full w-full bg-gradient-to-b from-white dark:from-primary/20 to-transparent pointer-events-none block rounded-lg"
              layoutId="hoverBackground"
              initial={{ opacity: 0 }}
              animate={{ opacity: 1, transition: { duration: 0.15 } }}
              exit={{ opacity: 0, transition: { duration: 0.15, delay: 0.2 } }}
            />
          )}
        </AnimatePresence>
        <div className="relative z-10 p-6">{props.children}</div>
      </div>
    );
  }
);
Card.displayName = "Card";

const CardHeader = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn("flex flex-col space-y-1.5 p-6", className)}
    {...props}
  />
));
CardHeader.displayName = "CardHeader";

